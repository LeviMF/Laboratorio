package Deposito;

public class Item {
    String escalaDeCor;
    String tamanho;
    String marca;
    String descricao;
    int codigo;
    int registroAnvisa;

    public Item(String escalaDeCor, String tamanho, String marca, String descricao, int codigo, int registroAnvisa) {
        this.escalaDeCor = escalaDeCor;
        this.tamanho = tamanho;
        this.marca = marca;
        this.descricao = descricao;
        this.codigo = codigo;
        this.registroAnvisa = registroAnvisa;
    }


}
